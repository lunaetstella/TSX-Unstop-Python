'''Experiment with different arithmetic and logical operators in the
interpreter'''
a = 10
b = 3

print("a + b =", a + b)       #Addition: 13
print("a - b =", a - b)      # Subtraction: 7
print("a * b =", a * b)      # Multiplication: 30
print("a / b =", a / b)      # Division: 3.3333...
print("a // b =", a // b)    # Floor Division (integer part): 3
print("a % b =", a % b)      # Modulus (remainder): 1
print("a ** b =", a ** b)    # Exponentiation: 10^3 = 1000

#logical operators
x = True
y = False

print("x and y =", x and y)  # Logical AND: False (both must be True)
print("x or y =", x or y)    # Logical OR: True (either can be True)
print("not x =", not x)      # Logical NOT: False (negates x)
print("not y =", not y)      # Logical NOT: True
